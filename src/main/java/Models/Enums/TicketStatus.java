package Models.Enums;

/**
 * Перелік можливих статусів квитка.
 * <p>
 * Цей enum визначає різні стани, в яких може перебувати квиток
 * протягом свого життєвого циклу, від моменту бронювання до його використання або скасування.
 * Кожен статус має асоційоване відображуване ім'я (displayName),
 * яке використовується для представлення статусу квитка користувачеві.
 * </p>
 *
 * @author [Ваше ім'я або назва команди, якщо доречно]
 * @version 1.0
 */
public enum TicketStatus {
    /**
     * Квиток заброньований, але ще не оплачений або не викуплений остаточно.
     * Відображуване ім'я: "Заброньований".
     */
    BOOKED("Заброньований"),

    /**
     * Квиток успішно проданий (оплачений).
     * Відображуване ім'я: "Проданий".
     */
    SOLD("Проданий"),

    /**
     * Квиток скасовано (наприклад, пасажиром або через скасування рейсу).
     * Відображуване ім'я: "Скасований".
     */
    CANCELLED("Скасований"),

    /**
     * Квиток був використаний для здійснення поїздки.
     * Цей статус може використовуватися для ведення історії поїздок.
     * Відображуване ім'я: "Використаний".
     */
    USED("Використаний"); // Додано для історії поїздок

    /**
     * Відображуване ім'я статусу квитка, призначене для показу користувачеві.
     * Наприклад, "Заброньований", "Проданий".
     */
    private final String displayName;

    /**
     * Конструктор для ініціалізації константи переліку з її відображуваним ім'ям.
     *
     * @param displayName Рядок, що представляє відображуване ім'я для статусу квитка.
     */
    TicketStatus(String displayName) {
        this.displayName = displayName;
    }

    /**
     * Повертає відображуване ім'я статусу квитка.
     * Це ім'я використовується для представлення статусу квитка в інтерфейсі користувача.
     *
     * @return Рядок, що є відображуваним ім'ям статусу квитка (наприклад, "Проданий").
     */
    public String getDisplayName() {
        return displayName;
    }

    /**
     * Повертає рядкове представлення об'єкта, яке є його відображуваним ім'ям.
     * Перевизначений для зручності відображення та відлагодження.
     *
     * @return Відображуване ім'я статусу квитка (наприклад, "Використаний").
     */
    @Override
    public String toString() {
        return displayName;
    }
}